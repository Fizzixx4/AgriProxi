namespace App\Repository;

use App\Entity\Entreprise;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Entreprise>
 *
 * @method Entreprise|null find($id, $lockMode = null, $lockVersion = null)
 * @method Entreprise|null findOneBy(array $criteria, array $orderBy = null)
 * @method Entreprise[]    findAll()
 * @method Entreprise[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class EntrepriseRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Entreprise::class);
    }

    public function save(Entreprise $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Entreprise $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function getLastId()
    {
        $connexion = $this->getEntityManager()->getConnection();

        $sql = 'SELECT max(e.id) FROM entreprise e';

        $result = $connexion->prepare($sql)->executeQuery();

        return $result->fetchOne();
    }

    public function findNameLike(string $string)
    {
        $string = strtoupper($string);
        return $this->createQueryBuilder('e')
            ->where('e.name LIKE :val')
            ->andWhere('e.typeEntreprise = 1')
            ->setParameter('val', $string . "%")
            ->setMaxResults('6')
            ->orderBy('e.name', 'ASC')
            ->getQuery()
            ->getResult();
    }

